/**
 * _realloc - using malloc to reallocated mem blockss.
 *@ptr: pointer.
 *@old_size: prev pointerr size.
 *@new_size: cuurrent pointerr size.
 *Return: Pointerr to re-allocated mem.
 */
void *_realloc(void *ptr, unsigned int old_size, unsigned int new_size)
{
	void *result;

	if (new_size == old_size)
		return (ptr);
	if (new_size == 0 && ptr)
	{
		free(ptr);
		return (NULL);
	}
	result = malloc(new_size);
	if (result == NULL)
	{
		free(result);
		return (NULL);
	}
	if (ptr == NULL)
	{
		fill_an_array(result, '\0', new_size);
		free(ptr);
	}
	else
	{
		_memcpy(result, ptr, old_size);
		free(ptr);
	}
	return (result);
}

/**
 * free_all - allocated mem release.
 * @cmd: pointerrs array
 * @line: pointerrs charaacter.
 * Return: Void.
 */
void free_all(char **cmd, char *line)
{
	free(cmd);
	free(line);
	cmd = NULL;
	line = NULL;
}

/**
 * _memcpy - bytes of mem copied from src dest.
 *@dest: pointerr of destiination.
 *@src: pointerr of source.
 *@n: size to be copied.
 *Return: destination's pointerr.
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
	{
		dest[i] = src[i];
	}
	return (dest);
}

/**
 * fill_an_array - constant byte to occupy.
 *@a: void pointer.
 *@el: int.
 *@len: lengtth for int.
 *Return: void pointerr.
 */
void *fill_an_array(void *a, int el, unsigned int len)
{
	char *p = a;
	unsigned int i = 0;

	while (i < len)
	{
		*p = el;
		p++;
		i++;
	}
	return (a);
}

/**
 * _calloc - malloc allocates mem for arrayy.
 *@size: sizee.
 *Return: void pointerr.
 */
void *_calloc(unsigned int size)
{
	char *a;
	unsigned int i;

	if (size == 0)
		return (NULL);
	a = malloc(size);
	if (a == NULL)
		return (NULL);
	for (i = 0; i < size; i++)
	{
		a[i] = '\0';
	}
	return (a);
}

