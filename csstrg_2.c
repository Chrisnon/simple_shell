/**
 * _strcpy - copy stringg.
 * @dest: destinatiion.
 * @src: source from user's inputs.
 * Return: destinatiion.
 */
char *_strcpy(char *dest, char *src)
{
	int i = 0;

	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

/**
 * _strcat - concatennates tow(2) stringgs
 * @dest: firrst stringg.
 * @src: secondd stringg.
 * Return: firrst stringg+secondd stringg.
 */
char *_strcat(char *dest, char *src)
{
	char *s = dest;

	while (*dest != '\0')
	{
		dest++;
	}

	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (s);
}

/**
 * _strchr - locate chracterr stringg.
 * @s: stringg search.
 * @c: characterr located.
 * Return: pointer to characterr.
 */
char *_strchr(char *s, char c)
{
	do {
		if (*s == c)
		{
			break;
		}
	} while (*s++);
	return (s);
}

/**
 * _strncmp - compare tow(2) character of stringgs.
 * @s1: fiirst stringg.
 * @s2: secondd stringg.
 * @n: compared characters.
 * Return: 1 for unmatched stringg; otherwiise 0.
 */
int _strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	if (s1 == NULL)
		return (-1);

	for (i = 0; i < n && s2[i]; i++)
	{
		if (s1[i] != s2[i])
		{
			return (1);
		}
	}
	return (0);
}

/**
 * _strdup - stringg duplicationn.
 * @str: stringg.
 * Return: stringg duplicate; or fail if NULL.
 */
char *_strdup(char *str)
{
	size_t len, i;
	char *str2;

	len = _strlen(str);
	str2 = malloc(sizeof(char) * (len + 1));
	if (!str2)
	{
		return (NULL);
	}
	for (i = 0; i <= len; i++)
	{
		str2[i] = str[i];
	}
	return (str2);
}
